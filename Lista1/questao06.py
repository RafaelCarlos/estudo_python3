#Calcule o tempo de uma viagem de carro. Pergunte a distância a percorrer e
# a velocidade média esperada para a viagem.
distancia = float(input("Digite a distancia a ser percorrida"))
velocidade = float(input("Digite a velocidade media"))
velocidade = (distancia / velocidade)
print("O tempo para chegada é %f" %velocidade, "H")
